public class SwitchStatementQuestion2 {

	public static void main(String[] args) {

		char gender = 'f';

		switch (gender) {

		case 'M':
			System.out.println("Male");
			break;

		case 'm':
			System.out.println("Male");
			break;
		case 'F':
			System.out.println("Female");
			break;
		case 'f':
			System.out.println("female");
			break;

		}

	}

}
